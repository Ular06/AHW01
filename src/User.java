public class User {
private String colors;
private String name;


    public String getColors() {
        return colors;
    }

    public void setColors(String colors) {
        this.colors = colors;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public User(String colors, String name) {
        this.colors = colors;
        this.name = name;



    }
}
